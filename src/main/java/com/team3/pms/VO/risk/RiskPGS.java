package com.team3.pms.VO.risk;

public class RiskPGS {
	private String riskpgs_cd;
	private String riskpgs_st;
	public RiskPGS() {
		super();
	}
	public RiskPGS(String riskpgs_cd, String riskpgs_st) {
		super();
		this.riskpgs_cd = riskpgs_cd;
		this.riskpgs_st = riskpgs_st;
	}
	public String getRiskpgs_cd() {
		return riskpgs_cd;
	}
	public void setRiskpgs_cd(String riskpgs_cd) {
		this.riskpgs_cd = riskpgs_cd;
	}
	public String getRiskpgs_st() {
		return riskpgs_st;
	}
	public void setRiskpgs_st(String riskpgs_st) {
		this.riskpgs_st = riskpgs_st;
	}
	
	
}
